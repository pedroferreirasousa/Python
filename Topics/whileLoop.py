#LOOPS WHILE -> É BASICAMENTE UMA ESTRUTURA EM PYTHON QUE NOS PERMITE PERCORRER E EXECUTAR UM BLOCO DE CODIGO VARIAS VEZES
#Posso coloca-lo e ele repetiria em looping e pararia apenas quando a contição passada for concretizada


#Noções BÁSICAS de como funciona:

i = 1

while i <= 10: #ENQUANtO "CONDIÇAO": nesse caso "Enquanto i for menor ou igual a 10"
  print(i)
  i += 1  # abreviando a logica de i = i + 1.


print("Finalizado o looping")

